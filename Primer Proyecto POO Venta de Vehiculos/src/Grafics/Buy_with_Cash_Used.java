        public void buySucursal(int placa) // Comprar autos usados ,el comprador es tipo persona
    {
            Vehicule autoTObuy=verifyCar(placa); 
            
            if(autoTObuy != null) // Step 1
            {
                if(sucursal.myCard.get(0).getMoney() > autoTObuy.getPrice() )// Step 2
                {                    
                    // aqui agrego el auto a mi list
                    Seler seler=userMet.ReturnObjetSeler(autoTObuy.getIncharge()); // this met return the user with only the name
                    autoTObuy.setIncharge(sucursal.getName());// change the name of incharge
                    //
                    sucursal.boughtCars.add(autoTObuy);// add the vehicle in my list to buy
                    //
                    Login.selerMet.selerVehiculesList.remove(autoTObuy);
                    //
                    Login.vehiculesMet.vehiculesList.remove(autoTObuy); //delate of all sistem 
                    seler.deleteCar(autoTObuy.getLicensePlate());//delate of list of seler
                    
                    sucursal.myCard.get(0).setMoney(sucursal.myCard.get(0).getMoney()-autoTObuy.getPrice() );//aqui resto el monto del comprador
                    
                    BillBuyer  nuevo=new BillBuyer(sucursal.getName(),seler.getName(),autoTObuy.getNewOrused(),autoTObuy.getPrice(),autoTObuy.getLicensePlate(),autoTObuy.getBrand());
                    sucursal.record.add(nuevo);// agrego a mi historial de compras 
                    
                    // agrego a todas las compras
                    Purchases nuevoPurhase = new Purchases(autoTObuy.getType(),autoTObuy.getBrand(),autoTObuy.getLicensePlate(),autoTObuy.getPrice());
                    sucursal.purchases.add(nuevoPurhase);
                    
                    
                    
                    JOptionPane.showMessageDialog(null, "Factura: "+"\n"+"# Placa: "+autoTObuy.getLicensePlate()+"\n"+"Antiguo encargado: "+seler.getName()+"\n"+"Precio: "+autoTObuy.getPrice());
        
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"No tiene el dinero suficiente");
                }
            }
    }
    
   public Vehicule verifyCar(int placa)
   {
       for(Vehicule i: Login.vehiculesMet.vehiculesList)
       {
           if( i.getLicensePlate()== placa)
           {
               return i;
           }
       }
       return null;
   }
